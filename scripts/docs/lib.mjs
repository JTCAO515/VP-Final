import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

export const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../..");

export const manifestPath = path.join(repoRoot, "docs/manifest.json");
export const indexPath = path.join(repoRoot, "docs/INDEX.md");

export function readManifest() {
  return JSON.parse(fs.readFileSync(manifestPath, "utf8"));
}

export function normalizePath(value) {
  return value.split(path.sep).join("/").replace(/^\.\//, "");
}

export function matchesPrefix(file, prefix) {
  const normalizedFile = normalizePath(file);
  const normalizedPrefix = normalizePath(prefix);
  return normalizedPrefix.endsWith("/")
    ? normalizedFile.startsWith(normalizedPrefix)
    : normalizedFile === normalizedPrefix;
}

function walkMarkdown(directory) {
  if (!fs.existsSync(directory)) return [];

  return fs.readdirSync(directory, { withFileTypes: true }).flatMap((entry) => {
    const absolute = path.join(directory, entry.name);
    if (entry.isDirectory()) return walkMarkdown(absolute);
    return entry.isFile() && entry.name.endsWith(".md") ? [absolute] : [];
  });
}

export function collectControlledMarkdown(manifest) {
  const paths = new Set();

  for (const target of manifest.controlledMarkdown) {
    const absolute = path.join(repoRoot, target);
    if (target.endsWith("/")) {
      for (const file of walkMarkdown(absolute)) {
        paths.add(normalizePath(path.relative(repoRoot, file)));
      }
    } else if (fs.existsSync(absolute)) {
      paths.add(normalizePath(target));
    }
  }

  for (const ignored of manifest.ignoredMarkdown ?? []) paths.delete(ignored);
  return [...paths].sort();
}

function indexLink(documentPath) {
  return documentPath.startsWith("docs/")
    ? documentPath.slice("docs/".length)
    : `../${documentPath}`;
}

function escapeCell(value) {
  return String(value).replaceAll("|", "\\|").replaceAll("\n", " ");
}

export function renderIndex(manifest) {
  const lines = [
    "# VisePanda Documentation Index",
    "",
    "<!-- Generated by `pnpm docs:index`. Do not edit this file directly. -->",
    "",
    "This index is generated from [`manifest.json`](manifest.json). Start with the **Start Here**",
    "section, then follow the module, constraint, or runbook relevant to the current GitHub Issue.",
    "All development follows [钱学森 Skills](methodology/qian-systems-engineering.md).",
    "",
  ];

  const categories = [...manifest.categories].sort((a, b) => a.order - b.order);
  for (const category of categories) {
    const documents = manifest.documents.filter((document) => document.category === category.id);
    if (documents.length === 0) continue;

    lines.push(`## ${category.title}`, "", category.description, "");
    lines.push("| Document | Type | Status | Owner | Purpose |");
    lines.push("| --- | --- | --- | --- | --- |");
    for (const document of documents.sort((a, b) => a.title.localeCompare(b.title))) {
      lines.push(
        `| [${escapeCell(document.title)}](${indexLink(document.path)}) | ${document.type} | ${document.status} | ${escapeCell(document.owner)} | ${escapeCell(document.summary)} |`,
      );
    }
    lines.push("");
  }

  lines.push(
    "## Maintenance",
    "",
    "- Add, move, archive, or reclassify a document in `docs/manifest.json`.",
    "- Run `pnpm docs:index` after registry changes.",
    "- Run `pnpm docs:check` for coverage, metadata, headings, links, and freshness.",
    "- Run `pnpm docs:impact -- --base <git-ref>` to verify code-to-document synchronization.",
  );

  return `${lines.join("\n")}\n`;
}

export function extractLocalLinks(markdown) {
  const links = [];
  const pattern = /\[[^\]]*\]\(([^)]+)\)/g;
  for (const match of markdown.matchAll(pattern)) {
    let target = match[1].trim();
    if (target.startsWith("<") && target.endsWith(">")) {
      target = target.slice(1, -1);
    }
    if (
      !target ||
      target.startsWith("#") ||
      target.startsWith("/") ||
      /^[a-z][a-z0-9+.-]*:/i.test(target)
    ) {
      continue;
    }
    target = target.split("#", 1)[0].split("?", 1)[0];
    if (!target) continue;
    try {
      links.push(decodeURIComponent(target));
    } catch {
      links.push(target);
    }
  }
  return links;
}
